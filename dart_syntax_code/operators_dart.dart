void main() {
  int n1 = 10;
  int n2 = 20;
  // Addition
  print(n1 + n2);
  // Subtraction
  print(n2 - n1);
  // Multiplication
  print(n1 * n2);
  // Division
  print(n1 / n2);
  // unary minus (Negation)
  print(-n1);
  // integer Division
  print(n1 ~/ n2);
  // Modules
  print(n1 % n2);
  // Increment
  n1++;
  print(n1);
  // Decrement
  n2--;
  print(n2);
}
